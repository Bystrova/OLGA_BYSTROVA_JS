function createImage() {
    var home = document.getElementById('home');
    var context = home.getContext('2d');
    context.strokeStyle = '#337B8F';
    context.lineWidth = 4;
    context.fillStyle = '#90CAD7';
    context.beginPath();
    context.arc(300, 600, 110, 0,  2*Math.PI, true);
    context.fill();
    context.closePath();
    context.stroke();
    context.beginPath();
    context.arc(750, 600, 110, 0,  2*Math.PI, true);
    context.fill();
    context.closePath();
    context.stroke();
    context.beginPath();
    context.arc(500, 600, 28, 0,  2*Math.PI, true);
    context.closePath();
    context.stroke();
    context.beginPath();
    context.moveTo(300, 600);
    context.lineTo(430, 460);
    context.moveTo(429, 460);
    context.lineTo(718, 460);
    context.moveTo(718, 460);
    context.lineTo(500, 600);
    context.moveTo(500, 600);
    context.lineTo(300, 600);
    context.moveTo(500, 600);
    context.lineTo(395, 410);
    context.moveTo(350, 410);
    context.lineTo(440, 410);
    context.moveTo(700, 390);
    context.lineTo(770, 325);
    context.moveTo(700, 390);
    context.lineTo(620, 410);
    context.closePath();
    context.stroke();
    context.beginPath();
    context.moveTo(750, 600);
    context.lineTo(700, 390);
    context.moveTo(480, 580);
    context.lineTo(460, 560);
    context.moveTo(520, 620);
    context.lineTo(540, 640);
    context.closePath();
    context.stroke();
}
createImage();
